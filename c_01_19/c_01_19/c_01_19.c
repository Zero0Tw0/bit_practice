//HJ40 统计字符
//
#include<stdio.h>

int main() {
    char str[1001];
    while (gets(str))
    {
        int a[4] = { 0 };
        int len = strlen(str);
        for (int i = 0; i < len; ++i)
        {
            if (str[i] >= 'a' && str[i] <= 'z' || str[i] >= 'A' && str[i] <= 'Z')
                a[0] ++;
            else if (str[i] == ' ')
                a[1] ++;
            else if (str[i] >= '0' && str[i] <= '9')
                a[2] ++;
            else
                a[3] ++;
        }
        for (int i = 0; i < 4; ++i)
            printf("%d\n", a[i]);
    }
    return 0;
}